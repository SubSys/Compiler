-- | Ensure all generators return a consistent naming representation, given any common input.
-- I.e. all have the same identifier!
--
-- E.g.
-- `newVar 1` == `E.Var' (ID.Ident_ 1)` and `newBinder 1` == `Etc.Binder_ (ID.Ident_ 1)`,
-- ^ idents are the same...




freshVar :: Int -> E.Expr
freshVar idx =
    E.Var' (freshIdent idx)

freshBinder :: Int -> Etc.Binder
freshBinder idx =
    Etc.Binder_ (freshIdent idx)

freshIdent :: Int -> ID.Ident
freshIdent idx =
    ID.Ident_ $ freshLabel idx


freshLabel :: Int -> Text
freshLabel idx =
    prefix `Text.append` Text.pack (Pre.show idx)
    where
        prefix :: Text
        prefix = Text.pack "!"



